#!/usr/bin/env node
/**
 * Embeds packages/coding-agent/templates/** into a generated TS module so
 * the Bun-compiled standalone binary can seed ~/.hoocode/{modes,profiles}
 * on first run without needing the templates/ folder next to the executable.
 *
 * Output: packages/coding-agent/src/init-templates.generated.ts
 *
 * Run from packages/coding-agent or via the package's `build` script.
 */
import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const packageDir = resolve(__dirname, "..");
const templatesDir = join(packageDir, "templates");
const outFile = join(packageDir, "src", "init-templates.generated.ts");

function escape(content) {
	// JSON.stringify produces a valid TS string literal (handles quotes, newlines,
	// backslashes, control chars) — safer than building backticks by hand.
	return JSON.stringify(content);
}

function readFlatMarkdown(dir) {
	const entries = {};
	let names;
	try {
		names = readdirSync(dir, { withFileTypes: true });
	} catch {
		return entries;
	}
	for (const dirent of names) {
		if (!dirent.isFile() || !dirent.name.endsWith(".md")) continue;
		const key = dirent.name.slice(0, -3);
		entries[key] = readFileSync(join(dir, dirent.name), "utf8");
	}
	return entries;
}

function readSubdirEntries(parent, filename) {
	const entries = {};
	let names;
	try {
		names = readdirSync(parent, { withFileTypes: true });
	} catch {
		return entries;
	}
	for (const dirent of names) {
		if (!dirent.isDirectory()) continue;
		const filePath = join(parent, dirent.name, filename);
		try {
			entries[dirent.name] = readFileSync(filePath, "utf8");
		} catch {
			// silently skip directories that don't have the expected file
		}
	}
	return entries;
}

const defaultConfig = readFileSync(join(templatesDir, "default-config.json"), "utf8");
const modes = readSubdirEntries(join(templatesDir, "modes"), "system.md");
const profiles = readSubdirEntries(join(templatesDir, "profiles"), "context.md");
const agentPrompts = readFlatMarkdown(join(templatesDir, "agents"));

const modeEntries = Object.entries(modes)
	.sort(([a], [b]) => a.localeCompare(b))
	.map(([name, content]) => `\t${escape(name)}: ${escape(content)},`)
	.join("\n");

const profileEntries = Object.entries(profiles)
	.sort(([a], [b]) => a.localeCompare(b))
	.map(([name, content]) => `\t${escape(name)}: ${escape(content)},`)
	.join("\n");

const agentEntries = Object.entries(agentPrompts)
	.sort(([a], [b]) => a.localeCompare(b))
	.map(([name, content]) => `\t${escape(name)}: ${escape(content)},`)
	.join("\n");

const banner = `// AUTO-GENERATED by scripts/embed-templates.mjs — do not edit.
// Source of truth: packages/coding-agent/templates/**
// Regenerated on every \`npm run build\`.
`;

const body = `${banner}
export const EMBEDDED_DEFAULT_CONFIG: string = ${escape(defaultConfig)};

export const EMBEDDED_MODES: Record<string, string> = {
${modeEntries}
};

export const EMBEDDED_PROFILES: Record<string, string> = {
${profileEntries}
};

export const EMBEDDED_AGENT_PROMPTS: Record<string, string> = {
${agentEntries}
};
`;

writeFileSync(outFile, body);
const modeCount = Object.keys(modes).length;
const profileCount = Object.keys(profiles).length;
const agentCount = Object.keys(agentPrompts).length;
console.log(
	`embed-templates: wrote ${outFile} (${modeCount} mode${modeCount === 1 ? "" : "s"}, ${profileCount} profile${profileCount === 1 ? "" : "s"}, ${agentCount} agent prompt${agentCount === 1 ? "" : "s"})`,
);
